#include <core/traits/is_integral.h>

namespace hud_test {
    class a;
    enum class b : u32;
}

TEST(traits, is_integral) {

    ASSERT_FALSE((hud::is_integral_v<hud_test::a>));
    ASSERT_FALSE((hud::is_integral_v<hud_test::b>));
    ASSERT_FALSE((hud::is_integral_v<f64>));
    ASSERT_FALSE((hud::is_integral_v<f32>));

    ASSERT_TRUE((hud::is_integral_v<bool>));
    ASSERT_TRUE((hud::is_integral_v<char>));
    ASSERT_TRUE((hud::is_integral_v<ansichar>));
    ASSERT_TRUE((hud::is_integral_v<char16_t>));
    ASSERT_TRUE((hud::is_integral_v<char32_t>));
    ASSERT_TRUE((hud::is_integral_v<char16_t>));
    ASSERT_TRUE((hud::is_integral_v<wchar_t>));
    ASSERT_TRUE((hud::is_integral_v<wchar>));
    ASSERT_TRUE((hud::is_integral_v<signed char>));
    ASSERT_TRUE((hud::is_integral_v<i8>));
    ASSERT_TRUE((hud::is_integral_v<short int>));
    ASSERT_TRUE((hud::is_integral_v<i16>));
    ASSERT_TRUE((hud::is_integral_v<int>));
    ASSERT_TRUE((hud::is_integral_v<i32>));
    ASSERT_TRUE((hud::is_integral_v<long int>));
    ASSERT_TRUE((hud::is_integral_v<i64>));
    ASSERT_TRUE((hud::is_integral_v<long long int>));
    ASSERT_TRUE((hud::is_integral_v<i64>));
    ASSERT_TRUE((hud::is_integral_v<long long>));
    ASSERT_TRUE((hud::is_integral_v<i64>));
    ASSERT_TRUE((hud::is_integral_v<unsigned char>));
    ASSERT_TRUE((hud::is_integral_v<u8>));
    ASSERT_TRUE((hud::is_integral_v<unsigned short int>));
    ASSERT_TRUE((hud::is_integral_v<u16>));
    ASSERT_TRUE((hud::is_integral_v<unsigned int>));
    ASSERT_TRUE((hud::is_integral_v<u32>));
    ASSERT_TRUE((hud::is_integral_v<unsigned long int>));
    ASSERT_TRUE((hud::is_integral_v<u64>));
    ASSERT_TRUE((hud::is_integral_v<unsigned long long int>));
    ASSERT_TRUE((hud::is_integral_v<u64>));

    ASSERT_TRUE((hud::is_integral_v<const bool>));
    ASSERT_TRUE((hud::is_integral_v<const char>));
    ASSERT_TRUE((hud::is_integral_v<const ansichar>));
    ASSERT_TRUE((hud::is_integral_v<const char16_t>));
    ASSERT_TRUE((hud::is_integral_v<const char32_t>));
    ASSERT_TRUE((hud::is_integral_v<const char16_t>));
    ASSERT_TRUE((hud::is_integral_v<const wchar_t>));
    ASSERT_TRUE((hud::is_integral_v<const wchar>));
    ASSERT_TRUE((hud::is_integral_v<const signed char>));
    ASSERT_TRUE((hud::is_integral_v<const i8>));
    ASSERT_TRUE((hud::is_integral_v<const short int>));
    ASSERT_TRUE((hud::is_integral_v<const i16>));
    ASSERT_TRUE((hud::is_integral_v<const int>));
    ASSERT_TRUE((hud::is_integral_v<const i32>));
    ASSERT_TRUE((hud::is_integral_v<const long int>));
    ASSERT_TRUE((hud::is_integral_v<const i64>));
    ASSERT_TRUE((hud::is_integral_v<const long long int>));
    ASSERT_TRUE((hud::is_integral_v<const i64>));
    ASSERT_TRUE((hud::is_integral_v<const long long>));
    ASSERT_TRUE((hud::is_integral_v<const i64>));
    ASSERT_TRUE((hud::is_integral_v<const unsigned char>));
    ASSERT_TRUE((hud::is_integral_v<const u8>));
    ASSERT_TRUE((hud::is_integral_v<const unsigned short int>));
    ASSERT_TRUE((hud::is_integral_v<const u16>));
    ASSERT_TRUE((hud::is_integral_v<const unsigned int>));
    ASSERT_TRUE((hud::is_integral_v<const u32>));
    ASSERT_TRUE((hud::is_integral_v<const unsigned long int>));
    ASSERT_TRUE((hud::is_integral_v<const u64>));
    ASSERT_TRUE((hud::is_integral_v<const unsigned long long int>));
    ASSERT_TRUE((hud::is_integral_v<const u64>));

    ASSERT_TRUE((hud::is_integral_v<const volatile bool>));
    ASSERT_TRUE((hud::is_integral_v<const volatile char>));
    ASSERT_TRUE((hud::is_integral_v<const volatile ansichar>));
    ASSERT_TRUE((hud::is_integral_v<const volatile char16_t>));
    ASSERT_TRUE((hud::is_integral_v<const volatile char32_t>));
    ASSERT_TRUE((hud::is_integral_v<const volatile char16_t>));
    ASSERT_TRUE((hud::is_integral_v<const volatile wchar_t>));
    ASSERT_TRUE((hud::is_integral_v<const volatile wchar>));
    ASSERT_TRUE((hud::is_integral_v<const volatile signed char>));
    ASSERT_TRUE((hud::is_integral_v<const volatile i8>));
    ASSERT_TRUE((hud::is_integral_v<const volatile short int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile i16>));
    ASSERT_TRUE((hud::is_integral_v<const volatile int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile i32>));
    ASSERT_TRUE((hud::is_integral_v<const volatile long int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile i64>));
    ASSERT_TRUE((hud::is_integral_v<const volatile long long int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile i64>));
    ASSERT_TRUE((hud::is_integral_v<const volatile long long>));
    ASSERT_TRUE((hud::is_integral_v<const volatile i64>));
    ASSERT_TRUE((hud::is_integral_v<const volatile unsigned char>));
    ASSERT_TRUE((hud::is_integral_v<const volatile u8>));
    ASSERT_TRUE((hud::is_integral_v<const volatile unsigned short int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile u16>));
    ASSERT_TRUE((hud::is_integral_v<const volatile unsigned int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile u32>));
    ASSERT_TRUE((hud::is_integral_v<const volatile unsigned long int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile u64>));
    ASSERT_TRUE((hud::is_integral_v<const volatile unsigned long long int>));
    ASSERT_TRUE((hud::is_integral_v<const volatile u64>));

    ASSERT_TRUE((hud::is_integral_v<volatile bool>));
    ASSERT_TRUE((hud::is_integral_v<volatile char>));
    ASSERT_TRUE((hud::is_integral_v<volatile ansichar>));
    ASSERT_TRUE((hud::is_integral_v<volatile char16_t>));
    ASSERT_TRUE((hud::is_integral_v<volatile char32_t>));
    ASSERT_TRUE((hud::is_integral_v<volatile char16_t>));
    ASSERT_TRUE((hud::is_integral_v<volatile wchar_t>));
    ASSERT_TRUE((hud::is_integral_v<volatile wchar>));
    ASSERT_TRUE((hud::is_integral_v<volatile signed char>));
    ASSERT_TRUE((hud::is_integral_v<volatile i8>));
    ASSERT_TRUE((hud::is_integral_v<volatile short int>));
    ASSERT_TRUE((hud::is_integral_v<volatile i16>));
    ASSERT_TRUE((hud::is_integral_v<volatile int>));
    ASSERT_TRUE((hud::is_integral_v<volatile i32>));
    ASSERT_TRUE((hud::is_integral_v<volatile long int>));
    ASSERT_TRUE((hud::is_integral_v<volatile i64>));
    ASSERT_TRUE((hud::is_integral_v<volatile long long int>));
    ASSERT_TRUE((hud::is_integral_v<volatile i64>));
    ASSERT_TRUE((hud::is_integral_v<volatile long long>));
    ASSERT_TRUE((hud::is_integral_v<volatile i64>));
    ASSERT_TRUE((hud::is_integral_v<volatile unsigned char>));
    ASSERT_TRUE((hud::is_integral_v<volatile u8>));
    ASSERT_TRUE((hud::is_integral_v<volatile unsigned short int>));
    ASSERT_TRUE((hud::is_integral_v<volatile u16>));
    ASSERT_TRUE((hud::is_integral_v<volatile unsigned int>));
    ASSERT_TRUE((hud::is_integral_v<volatile u32>));
    ASSERT_TRUE((hud::is_integral_v<volatile unsigned long int>));
    ASSERT_TRUE((hud::is_integral_v<volatile u64>));
    ASSERT_TRUE((hud::is_integral_v<volatile unsigned long long int>));
    ASSERT_TRUE((hud::is_integral_v<volatile u64>));
}
